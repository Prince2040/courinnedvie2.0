<dl>
<dt><a name="ch.3.1"> 3.1</a><dd> Malheur &agrave; la ville rebelle et souill&eacute;e, A la ville pleine d'oppresseurs!
<dt><a name="ch.3.2"> 3.2</a><dd> Elle n'&eacute;coute aucune voix, Elle n'a point &eacute;gard &agrave; la correction, Elle ne se confie pas en l'&Eacute;ternel, Elle ne s'approche pas de son Dieu.
<dt><a name="ch.3.3"> 3.3</a><dd> Ses chefs au milieu d'elle sont des lions rugissants; Ses juges sont des loups du soir qui ne gardent rien pour le matin.
<dt><a name="ch.3.4"> 3.4</a><dd> Ses proph&egrave;tes sont t&eacute;m&eacute;raires, infid&egrave;les; Ses sacrificateurs profanent les choses saintes, violent la loi.
<dt><a name="ch.3.5"> 3.5</a><dd> L'&Eacute;ternel est juste au milieu d'elle, Il ne commet point d'iniquit&eacute;; Chaque matin il produit &agrave; la lumi&egrave;re ses jugements, Sans jamais y manquer; Mais celui qui est inique ne conna&icirc;t pas la honte.
<dt><a name="ch.3.6"> 3.6</a><dd> J'ai extermin&eacute; des nations; leurs tours sont d&eacute;truites; J'ai d&eacute;vast&eacute; leurs rues, plus de passants! Leurs villes sont ravag&eacute;es, plus d'hommes, plus d'habitants!
<dt><a name="ch.3.7"> 3.7</a><dd> Je disais: Si du moins tu voulais me craindre, Avoir &eacute;gard &agrave; la correction, Ta demeure ne serait pas d&eacute;truite, Tous les ch&acirc;timents dont je t'ai menac&eacute;e n'arriveraient pas; Mais ils se sont h&acirc;t&eacute;s de pervertir toutes leurs actions.
<dt><a name="ch.3.8"> 3.8</a><dd> Attendez-moi donc, dit l'&Eacute;ternel, Au jour o&ugrave; je me l&egrave;verai pour le butin, Car j'ai r&eacute;solu de rassembler les nations, De rassembler les royaumes, Pour r&eacute;pandre sur eux ma fureur, Toute l'ardeur de ma col&egrave;re; Car par le feu de ma jalousie tout le pays sera consum&eacute;.
<dt><a name="ch.3.9"> 3.9</a><dd> Alors je donnerai aux peuples des l&egrave;vres pures, Afin qu'ils invoquent tous le nom de l'&Eacute;ternel, Pour le servir d'un commun accord.
<dt><a name="ch.3.10"> 3.10</a><dd> D'au del&agrave; des fleuves de l'&Eacute;thiopie Mes adorateurs, mes dispers&eacute;s, m'apporteront des offrandes.
<dt><a name="ch.3.11"> 3.11</a><dd> En ce jour-l&agrave;, tu n'auras plus &agrave; rougir de toutes tes actions Par lesquelles tu as p&eacute;ch&eacute; contre moi; Car alors j'&ocirc;terai du milieu de toi ceux qui triomphaient avec arrogance, Et tu ne t'enorgueilliras plus sur ma montagne sainte.
<dt><a name="ch.3.12"> 3.12</a><dd> Je laisserai au milieu de toi un peuple humble et petit, Qui trouvera son refuge dans le nom de l'&Eacute;ternel.
<dt><a name="ch.3.13"> 3.13</a><dd> Les restes d'Isra&euml;l ne commettront point d'iniquit&eacute;, Ils ne diront point de mensonges, Et il ne se trouvera pas dans leur bouche une langue trompeuse; Mais ils pa&icirc;tront, ils se reposeront, et personne ne les troublera.
<dt><a name="ch.3.14"> 3.14</a><dd> Pousse des cris de joie, fille de Sion! Pousse des cris d'all&eacute;gresse, Isra&euml;l! R&eacute;jouis-toi et triomphe de tout ton coeur, fille de J&eacute;rusalem!
<dt><a name="ch.3.15"> 3.15</a><dd> L'&Eacute;ternel a d&eacute;tourn&eacute; tes ch&acirc;timents, Il a &eacute;loign&eacute; ton ennemi; Le roi d'Isra&euml;l, l'&Eacute;ternel, est au milieu de toi; Tu n'as plus de malheur &agrave; &eacute;prouver.
<dt><a name="ch.3.16"> 3.16</a><dd> En ce jour-l&agrave;, on dira &agrave; J&eacute;rusalem: Ne crains rien! Sion, que tes mains ne s'affaiblissent pas!
<dt><a name="ch.3.17"> 3.17</a><dd> L'&Eacute;ternel, ton Dieu, est au milieu de toi, comme un h&eacute;ros qui sauve; Il fera de toi sa plus grande joie; Il gardera le silence dans son amour; Il aura pour toi des transports d'all&eacute;gresse.
<dt><a name="ch.3.18"> 3.18</a><dd> Je rassemblerai ceux qui sont dans la tristesse, loin des f&ecirc;tes solennelles, Ceux qui sont sortis de ton sein; L'opprobre p&egrave;se sur eux.
<dt><a name="ch.3.19"> 3.19</a><dd> Voici, en ce temps-l&agrave;, j'agirai contre tous tes oppresseurs; Je d&eacute;livrerai les boiteux et je recueillerai ceux qui ont &eacute;t&eacute; chass&eacute;s, Je ferai d'eux un sujet de louange et de gloire Dans tous les pays o&ugrave; ils sont en opprobre.
<dt><a name="ch.3.20"> 3.20</a><dd> En ce temps-l&agrave;, je vous ram&egrave;nerai; En ce temps-l&agrave;, je vous rassemblerai; Car je ferai de vous un sujet de gloire et de louange Parmi tous les peuples de la terre, Quand je ram&egrave;nerai vos captifs sous vos yeux, Dit l'&Eacute;ternel.
</dl>



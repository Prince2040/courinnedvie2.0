<dl>
<dt><a name="ch.1.1"> 1.1</a><dd> La parole de l'&Eacute;ternel qui fut adress&eacute;e &agrave; Sophonie, fils de Cuschi, fils de Guedalia, fils d'Amaria, fils d'&Eacute;z&eacute;chias, au temps de Josias, fils d'Amon, roi de Juda.
<dt><a name="ch.1.2"> 1.2</a><dd> Je d&eacute;truirai tout sur la face de la terre, Dit l'&Eacute;ternel.
<dt><a name="ch.1.3"> 1.3</a><dd> Je d&eacute;truirai les hommes et les b&ecirc;tes, Les oiseaux du ciel et les poissons de la mer, Les objets de scandale, et les m&eacute;chants avec eux; J'exterminerai les hommes de la face de la terre, Dit l'&Eacute;ternel.
<dt><a name="ch.1.4"> 1.4</a><dd> J'&eacute;tendrai ma main sur Juda, Et sur tous les habitants de J&eacute;rusalem; J'exterminerai de ce lieu les restes de Baal, Le nom de ses ministres et les pr&ecirc;tres avec eux,
<dt><a name="ch.1.5"> 1.5</a><dd> Ceux qui se prosternent sur les toits devant l'arm&eacute;e des cieux, Ceux qui se prosternent en jurant par l'&Eacute;ternel Et en jurant par leur roi,
<dt><a name="ch.1.6"> 1.6</a><dd> Ceux qui se sont d&eacute;tourn&eacute;s de l'&Eacute;ternel, Et ceux qui ne cherchent pas l'&Eacute;ternel, Qui ne le consultent pas.
<dt><a name="ch.1.7"> 1.7</a><dd> Silence devant le Seigneur, l'&Eacute;ternel! Car le jour de l'&Eacute;ternel est proche, Car l'&Eacute;ternel a pr&eacute;par&eacute; le sacrifice, Il a choisi ses convi&eacute;s.
<dt><a name="ch.1.8"> 1.8</a><dd> Au jour du sacrifice de l'&Eacute;ternel, Je ch&acirc;tierai les princes et les fils du roi, Et tous ceux qui portent des v&ecirc;tements &eacute;trangers.
<dt><a name="ch.1.9"> 1.9</a><dd> En ce jour-l&agrave;, je ch&acirc;tierai tous ceux qui sautent par-dessus le seuil, Ceux qui remplissent de violence et de fraude la maison de leur ma&icirc;tre.
<dt><a name="ch.1.10"> 1.10</a><dd> En ce jour-l&agrave;, dit l'&Eacute;ternel, Il y aura des cris &agrave; la porte des poissons, Des lamentations dans l'autre quartier de la ville, Et un grand d&eacute;sastre sur les collines.
<dt><a name="ch.1.11"> 1.11</a><dd> G&eacute;missez, habitants de Macthesch! Car tous ceux qui trafiquent sont d&eacute;truits, Tous les hommes charg&eacute;s d'argent sont extermin&eacute;s.
<dt><a name="ch.1.12"> 1.12</a><dd> En ce temps-l&agrave;, je fouillerai J&eacute;rusalem avec des lampes, Et je ch&acirc;tierai les hommes qui reposent sur leurs lies, Et qui disent dans leur coeur: L'&Eacute;ternel ne fait ni bien ni mal.
<dt><a name="ch.1.13"> 1.13</a><dd> Leurs biens seront au pillage, Et leurs maisons seront d&eacute;vast&eacute;es; Ils auront b&acirc;ti des maisons, qu'ils n'habiteront plus, Ils auront plant&eacute; des vignes, dont ils ne boiront plus le vin.
<dt><a name="ch.1.14"> 1.14</a><dd> Le grand jour de l'&Eacute;ternel est proche, Il est proche, il arrive en toute h&acirc;te; Le jour de l'&Eacute;ternel fait entendre sa voix, Et le h&eacute;ros pousse des cris amers.
<dt><a name="ch.1.15"> 1.15</a><dd> Ce jour est un jour de fureur, Un jour de d&eacute;tresse et d'angoisse, Un jour de ravage et de destruction, Un jour de t&eacute;n&egrave;bres et d'obscurit&eacute;, Un jour de nu&eacute;es et de brouillards,
<dt><a name="ch.1.16"> 1.16</a><dd> Un jour o&ugrave; retentiront la trompette et les cris de guerre Contre les villes fortes et les tours &eacute;lev&eacute;es.
<dt><a name="ch.1.17"> 1.17</a><dd> Je mettrai les hommes dans la d&eacute;tresse, Et ils marcheront comme des aveugles, Parce qu'ils ont p&eacute;ch&eacute; contre l'&Eacute;ternel; Je r&eacute;pandrai leur sang comme de la poussi&egrave;re, Et leur chair comme de l'ordure.
<dt><a name="ch.1.18"> 1.18</a><dd> Ni leur argent ni leur or ne pourront les d&eacute;livrer, Au jour de la fureur de l'&Eacute;ternel; Par le feu de sa jalousie tout le pays sera consum&eacute;; Car il d&eacute;truira soudain tous les habitants du pays.
</dl>

<p style="margin-bottom:20px;padding-top:20px;" class="text-center"><span style="font-size:19px; color:#fdcc52"><b><a name="ch.2"></a>Sophonie  2</b></span><br><a href="#ch.1">Top</a></p>
<dl>
<dt><a name="ch.2.1"> 2.1</a><dd> Rentrez en vous-m&ecirc;mes, examinez-vous, Nation sans pudeur,
<dt><a name="ch.2.2"> 2.2</a><dd> Avant que le d&eacute;cret s'ex&eacute;cute Et que ce jour passe comme la balle, Avant que la col&egrave;re ardente de l'&Eacute;ternel fonde sur vous, Avant que le jour de la col&egrave;re de l'&Eacute;ternel fonde sur vous!
<dt><a name="ch.2.3"> 2.3</a><dd> Cherchez l'&Eacute;ternel, vous tous, humbles du pays, Qui pratiquez ses ordonnances! Recherchez la justice, recherchez l'humilit&eacute;! Peut-&ecirc;tre serez-vous &eacute;pargn&eacute;s au jour de la col&egrave;re de l'&Eacute;ternel.
<dt><a name="ch.2.4"> 2.4</a><dd> Car Gaza sera d&eacute;laiss&eacute;e, Askalon sera r&eacute;duite en d&eacute;sert, Asdod sera chass&eacute;e en plein midi, &Eacute;kron sera d&eacute;racin&eacute;e.
<dt><a name="ch.2.5"> 2.5</a><dd> Malheur aux habitants des c&ocirc;tes de la mer, &agrave; la nation des K&eacute;r&eacute;thiens! L'&Eacute;ternel a parl&eacute; contre toi, Canaan, pays des Philistins! Je te d&eacute;truirai, tu n'auras plus d'habitants.
<dt><a name="ch.2.6"> 2.6</a><dd> Les c&ocirc;tes de la mer seront des p&acirc;turages, des demeures pour les bergers, Et des parcs pour les troupeaux.
<dt><a name="ch.2.7"> 2.7</a><dd> Ces c&ocirc;tes seront pour les restes de la maison de Juda; C'est l&agrave; qu'ils pa&icirc;tront; Ils reposeront le soir dans les maisons d'Askalon; Car l'&Eacute;ternel, leur Dieu, ne les oubliera pas, Et il ram&egrave;nera leurs captifs.
<dt><a name="ch.2.8"> 2.8</a><dd> J'ai entendu les injures de Moab Et les outrages des enfants d'Ammon, Quand ils insultaient mon peuple Et s'&eacute;levaient avec arrogance contre ses fronti&egrave;res.
<dt><a name="ch.2.9"> 2.9</a><dd> C'est pourquoi, je suis vivant! dit l'&Eacute;ternel des arm&eacute;es, le Dieu d'Isra&euml;l, Moab sera comme Sodome, et les enfants d'Ammon comme Gomorrhe, Un lieu couvert de ronces, une mine de sel, un d&eacute;sert pour toujours; Le reste de mon peuple les pillera, Le reste de ma nation les poss&eacute;dera.
<dt><a name="ch.2.10"> 2.10</a><dd> Cela leur arrivera pour leur orgueil, Parce qu'ils ont insult&eacute; et trait&eacute; avec arrogance Le peuple de l'&Eacute;ternel des arm&eacute;es.
<dt><a name="ch.2.11"> 2.11</a><dd> L'&Eacute;ternel sera terrible contre eux, Car il an&eacute;antira tous les dieux de la terre; Et chacun se prosternera devant lui dans son pays, Dans toutes les &icirc;les des nations.
<dt><a name="ch.2.12"> 2.12</a><dd> Vous aussi, &Eacute;thiopiens, Vous serez frapp&eacute;s par mon &eacute;p&eacute;e.
<dt><a name="ch.2.13"> 2.13</a><dd> Il &eacute;tendra sa main sur le septentrion, Il d&eacute;truira l'Assyrie, Et il fera de Ninive une solitude, Une terre aride comme le d&eacute;sert.
<dt><a name="ch.2.14"> 2.14</a><dd> Des troupeaux se coucheront au milieu d'elle, Des animaux de toute esp&egrave;ce; Le p&eacute;lican et le h&eacute;risson Habiteront parmi les chapiteaux de ses colonnes; Des cris retentiront aux fen&ecirc;tres; La d&eacute;vastation sera sur le seuil, Car les lambris de c&egrave;dre seront arrach&eacute;s.
<dt><a name="ch.2.15"> 2.15</a><dd> Voil&agrave; donc cette ville joyeuse, Qui s'assied avec assurance, Et qui dit en son coeur: Moi, et rien que moi! Eh quoi! elle est en ruines, C'est un repaire pour les b&ecirc;tes! Tous ceux qui passeront pr&egrave;s d'elle Siffleront et agiteront la main.
</dl>


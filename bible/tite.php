<dl>
<dt><a name="ch.1.1"> 1.1</a><dd> Paul, serviteur de Dieu, et ap&ocirc;tre de J&eacute;sus Christ pour la foi des &eacute;lus de Dieu et la connaissance de la v&eacute;rit&eacute; qui est selon la pi&eacute;t&eacute;, -
<dt><a name="ch.1.2"> 1.2</a><dd> lesquelles reposent sur l'esp&eacute;rance de la vie &eacute;ternelle, promise d&egrave;s les plus anciens temps par le Dieu qui ne ment point,
<dt><a name="ch.1.3"> 1.3</a><dd> et qui a manifest&eacute; sa parole en son temps par la pr&eacute;dication qui m'a &eacute;t&eacute; confi&eacute;e d'apr&egrave;s l'ordre de Dieu notre Sauveur, -
<dt><a name="ch.1.4"> 1.4</a><dd> &agrave; Tite, mon enfant l&eacute;gitime en notre commune foi: que la gr&acirc;ce et la paix te soient donn&eacute;es de la part de Dieu le P&egrave;re et de J&eacute;sus Christ notre Sauveur!
<dt><a name="ch.1.5"> 1.5</a><dd> Je t'ai laiss&eacute; en Cr&egrave;te, afin que tu mettes en ordre ce qui reste &agrave; r&eacute;gler, et que, selon mes instructions, tu &eacute;tablisses des anciens dans chaque ville,
<dt><a name="ch.1.6"> 1.6</a><dd> s'il s'y trouve quelque homme irr&eacute;prochable, mari d'une seul femme, ayant des enfants fid&egrave;les, qui ne soient ni accus&eacute;s de d&eacute;bauche ni rebelles.
<dt><a name="ch.1.7"> 1.7</a><dd> Car il faut que l'&eacute;v&ecirc;que soit irr&eacute;prochable, comme &eacute;conome de Dieu; qu'il ne soit ni arrogant, ni col&egrave;re, ni adonn&eacute; au vin, ni violent, ni port&eacute; &agrave; un gain d&eacute;shonn&ecirc;te;
<dt><a name="ch.1.8"> 1.8</a><dd> mais qu'il soit hospitalier, ami des gens de bien, mod&eacute;r&eacute;, juste, saint, temp&eacute;rant,
<dt><a name="ch.1.9"> 1.9</a><dd> attach&eacute; &agrave; la vraie parole telle qu'elle a &eacute;t&eacute; enseign&eacute;e, afin d'&ecirc;tre capable d'exhorter selon la saine doctrine et de r&eacute;futer les contradicteurs.
<dt><a name="ch.1.10"> 1.10</a><dd> Il y a, en effet, surtout parmi les circoncis, beaucoup de gens rebelles, de vains discoureurs et de s&eacute;ducteurs,
<dt><a name="ch.1.11"> 1.11</a><dd> auxquels il faut fermer la bouche. Ils bouleversent des familles enti&egrave;res, enseignant pour un gain honteux ce qu'on ne doit pas enseigner.
<dt><a name="ch.1.12"> 1.12</a><dd> L'un d'entre eux, leur propre proph&egrave;te, a dit: Cr&eacute;tois toujours menteurs, m&eacute;chantes b&ecirc;tes, ventres paresseux.
<dt><a name="ch.1.13"> 1.13</a><dd> Ce t&eacute;moignage est vrai. C'est pourquoi reprends-les s&eacute;v&egrave;rement, afin qu'ils aient une foi saine,
<dt><a name="ch.1.14"> 1.14</a><dd> et qu'ils ne s'attachent pas &agrave; des fables juda&iuml;ques et &agrave; des commandements d'hommes qui se d&eacute;tournent de la v&eacute;rit&eacute;.
<dt><a name="ch.1.15"> 1.15</a><dd> Tout est pur pour ceux qui sont purs; mais rien n'est pur pour ceux qui sont souill&eacute;es et incr&eacute;dules, leur intelligence et leur conscience sont souill&eacute;s.
<dt><a name="ch.1.16"> 1.16</a><dd> Ils font profession de conna&icirc;tre Dieu, mais ils le renient par leurs oeuvres, &eacute;tant abominables, rebelles, et incapables d'aucune bonne oeuvre.
</dl>

<p style="margin-bottom:20px;padding-top:20px;" class="text-center"><span style="font-size:19px; color:#fdcc52"><b><a name="ch.2"></a>Tite  2</b></span><br><a href="#ch.1">Top</a></p>
<dl>
<dt><a name="ch.2.1"> 2.1</a><dd> Pour toi, dis les choses qui sont conformes &agrave; la saine doctrine.
<dt><a name="ch.2.2"> 2.2</a><dd> Dis que les vieillards doivent &ecirc;tre sobres, honn&ecirc;tes, mod&eacute;r&eacute;s, sains dans la foi, dans la charit&eacute;, dans la patience.
<dt><a name="ch.2.3"> 2.3</a><dd> Dis que les femmes &acirc;g&eacute;es doivent aussi avoir l'ext&eacute;rieur qui convient &agrave; la saintet&eacute;, n'&ecirc;tre ni m&eacute;disantes, ni adonn&eacute;es au vin; qu'elles doivent donner de bonnes instructions,
<dt><a name="ch.2.4"> 2.4</a><dd> dans le but d'apprendre aux jeunes femmes &agrave; aimer leurs maris et leurs enfants,
<dt><a name="ch.2.5"> 2.5</a><dd> &agrave; &ecirc;tre retenues, chastes, occup&eacute;es aux soins domestiques, bonnes, soumises &agrave; leurs maris, afin que la parole de Dieu ne soit pas blasph&eacute;m&eacute;e.
<dt><a name="ch.2.6"> 2.6</a><dd> Exhorte de m&ecirc;me les jeunes gens &agrave; &ecirc;tre mod&eacute;r&eacute;s,
<dt><a name="ch.2.7"> 2.7</a><dd> te montrant toi-m&ecirc;me &agrave; tous &eacute;gards un mod&egrave;le de bonnes oeuvres, et donnant un enseignement pur, digne,
<dt><a name="ch.2.8"> 2.8</a><dd> une parole saine, irr&eacute;prochable, afin que l'adversaire soit confus, n'ayant aucun mal &agrave; dire de nous.
<dt><a name="ch.2.9"> 2.9</a><dd> Exhorte les serviteurs &agrave; &ecirc;tre soumis &agrave; leurs ma&icirc;tres, &agrave; leur plaire en toutes choses, &agrave; n'&ecirc;tre point contredisants,
<dt><a name="ch.2.10"> 2.10</a><dd> &agrave; ne rien d&eacute;rober, mais &agrave; montrer toujours une parfaite fid&eacute;lit&eacute;, afin de faire honorer en tout la doctrine de Dieu notre Sauveur.
<dt><a name="ch.2.11"> 2.11</a><dd> Car la gr&acirc;ce de Dieu, source de salut pour tous les hommes, a &eacute;t&eacute; manifest&eacute;e.
<dt><a name="ch.2.12"> 2.12</a><dd> Elle nous enseigne &agrave; renoncer &agrave; l'impi&eacute;t&eacute; et aux convoitises mondaines, et &agrave; vivre dans le si&egrave;cle pr&eacute;sent selon la sagesse, la justice et la pi&eacute;t&eacute;,
<dt><a name="ch.2.13"> 2.13</a><dd> en attendant la bienheureuse esp&eacute;rance, et la manifestation de la gloire du grand Dieu et de notre Sauveur J&eacute;sus Christ,
<dt><a name="ch.2.14"> 2.14</a><dd> qui s'est donn&eacute; lui-m&ecirc;me pour nous, afin de nous racheter de toute iniquit&eacute;, et de se faire un peuple qui lui appartienne, purifi&eacute; par lui et z&eacute;l&eacute; pour les bonnes oeuvres.
<dt><a name="ch.2.15"> 2.15</a><dd> Dis ces choses, exhorte, et reprends, avec une pleine autorit&eacute;. Que personne ne te m&eacute;prise.
</dl>

<p style="margin-bottom:20px;padding-top:20px;" class="text-center"><span style="font-size:19px; color:#fdcc52"><b><a name="ch.3"></a>Tite  3</b></span><br><a href="#ch.1">Top</a></p>
<dl>
<dt><a name="ch.3.1"> 3.1</a><dd> Rappelle-leur d'&ecirc;tre soumis aux magistrats et aux autorit&eacute;s, d'ob&eacute;ir, d'&ecirc;tre pr&ecirc;ts &agrave; toute bonne oeuvre,
<dt><a name="ch.3.2"> 3.2</a><dd> de ne m&eacute;dire de personne, d'&ecirc;tre pacifiques, mod&eacute;r&eacute;s, pleins de douceur envers tous les hommes.
<dt><a name="ch.3.3"> 3.3</a><dd> Car nous aussi, nous &eacute;tions autrefois insens&eacute;s, d&eacute;sob&eacute;issants, &eacute;gar&eacute;s, asservis &agrave; toute esp&egrave;ce de convoitises et de volupt&eacute;s, vivant dans la m&eacute;chancet&eacute; et dans l'envie, dignes d'&ecirc;tre ha&iuml;s, et nous ha&iuml;ssant les uns les autres.
<dt><a name="ch.3.4"> 3.4</a><dd> Mais, lorsque la bont&eacute; de Dieu notre Sauveur et son amour pour les hommes ont &eacute;t&eacute; manifest&eacute;s,
<dt><a name="ch.3.5"> 3.5</a><dd> il nous a sauv&eacute;s, non &agrave; cause des oeuvres de justice que nous aurions faites, mais selon sa mis&eacute;ricorde, par le bapt&ecirc;me de la r&eacute;g&eacute;n&eacute;ration et le renouvellement du Saint Esprit,
<dt><a name="ch.3.6"> 3.6</a><dd> qu'il a r&eacute;pandu sur nous avec abondance par J&eacute;sus Christ notre Sauveur,
<dt><a name="ch.3.7"> 3.7</a><dd> afin que, justifi&eacute;s par sa gr&acirc;ce, nous devenions, en esp&eacute;rance, h&eacute;ritiers de la vie &eacute;ternelle.
<dt><a name="ch.3.8"> 3.8</a><dd> Cette parole est certaine, et je veux que tu affirmes ces choses, afin que ceux qui ont cru en Dieu s'appliquent &agrave; pratiquer de bonnes oeuvres.
<dt><a name="ch.3.9"> 3.9</a><dd> Voil&agrave; ce qui est bon et utile aux hommes. Mais &eacute;vite les discussions folles, les g&eacute;n&eacute;alogies, les querelles, les disputes relatives &agrave; la loi; car elles sont inutiles et vaines.
<dt><a name="ch.3.10"> 3.10</a><dd> &Eacute;loigne de toi, apr&egrave;s un premier et un second avertissement, celui qui provoque des divisions,
<dt><a name="ch.3.11"> 3.11</a><dd> sachant qu'un homme de cette esp&egrave;ce est perverti, et qu'il p&egrave;che, en se condamnant lui-m&ecirc;me.
<dt><a name="ch.3.12"> 3.12</a><dd> Lorsque je t'enverrai Art&eacute;mas ou Tychique, h&acirc;te-toi de venir me rejoindre &agrave; Nicopolis; car c'est l&agrave; que j'ai r&eacute;solu de passer l'hiver.
<dt><a name="ch.3.13"> 3.13</a><dd> Aie soin de pourvoir au voyage de Z&eacute;nas, le docteur de la loi, et d'Apollos, en sorte que rien ne leur manque.
<dt><a name="ch.3.14"> 3.14</a><dd> Il faut que les n&ocirc;tres aussi apprennent &agrave; pratiquer de bonnes oeuvres pour subvenir aux besoins pressants, afin qu'ils ne soient pas sans produire des fruits.
<dt><a name="ch.3.15"> 3.15</a><dd> Tous ceux qui sont avec moi te saluent. Salue ceux qui nous aiment dans la foi. Que la gr&acirc;ce soit avec vous tous!
</dl>


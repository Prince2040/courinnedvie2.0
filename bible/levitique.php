                            tes offrandes tu mettras du sel.</dd>

                        <dt>
                            <a name="ch.2.14"></a>2.14
                        </dt>

                        <dd>
                            &nbsp;Si tu fais &agrave; l'&Eacute;ternel une offrande des pr&eacute;mices,
                            tu pr&eacute;senteras des &eacute;pis nouveaux, r&ocirc;tis au feu et broy&eacute;s,

                        <dt>
                            <a name="ch.5.4"></a>5.4
                        </dt>

                        <dd>
                            &nbsp;Lorsque quelqu'un, parlant &agrave; la l&eacute;g&egrave;re, jure
                            de faire du mal ou du bien, et que, ne l'ayant pas remarqu&eacute; d'abord,
                            il s'en aper&ccedil;oive plus tard, il en sera coupable.</dd>

                        <dt>
                            <a name="ch.5.5"></a>5.5
                        </dt>

                        <dd>

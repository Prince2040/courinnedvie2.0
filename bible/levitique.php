                        <dt>
                            <a name="ch.15.10"></a>15.10
                        </dt>

                        <dd>
                            &nbsp;Celui qui touchera une chose quelconque qui a &eacute;t&eacute; sous
                            lui sera impur jusqu'au soir; et celui qui la portera lavera ses v&ecirc;tements,

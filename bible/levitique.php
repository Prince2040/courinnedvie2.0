                        <dt>
                            <a name="ch.2.15"></a>2.15
                        </dt>

                        <dd>
                            &nbsp;Tu verseras de l'huile dessus, et tu y ajouteras de l'encens; c'est
                            une offrande.</dd>

                        <dt>
                            <a name="ch.2.16"></a>2.16
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur br&ucirc;lera comme souvenir une portion des &eacute;pis
                            broy&eacute;s et de l'huile, avec tout l'encens. C'est une offrande consum&eacute;e
                            par le feu devant l'&Eacute;ternel.</dd>
                    </dl>


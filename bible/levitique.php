                    <p style="margin-bottom:20px;padding-top:20px;" class="text-center"><span style="font-size:19px; color:#fdcc52"><b><a name="ch.13"></a>L&eacute;vitique 13</b></span><br><a href="#ch.1">Top</a></p>

                    <dl>
                        <dt>
                            <a name="ch.13.1"></a>13.1
                        </dt>

                        <dd>
                            &nbsp;L'&Eacute;ternel parla &agrave; Mo&iuml;se et &agrave; Aaron, et
                            dit:</dd>

                        <dt>
                            <a name="ch.13.2"></a>13.2
                        </dt>

                        <dd>
                            &nbsp;Lorsqu'un homme aura sur la peau de son corps une tumeur, une dartre,
                            ou une tache blanche, qui ressemblera &agrave; une plaie de l&egrave;pre
                            sur la peau de son corps, on l'am&egrave;nera au sacrificateur Aaron, ou
                            &agrave; l'un de ses fils qui sont sacrificateurs.</dd>

                        <dt>
                            <a name="ch.13.3"></a>13.3
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur examinera la plaie qui est sur la peau du corps.
                            Si le poil de la plaie est devenu blanc, et que la plaie paraisse plus
                            profonde que la peau du corps, c'est une plaie de l&egrave;pre: le sacrificateur
                            qui aura fait l'examen d&eacute;clarera cet homme impur.</dd>

                        <dt>
                            <a name="ch.13.4"></a>13.4
                        </dt>

                        <dd>
                            &nbsp;S'il y a sur la peau du corps une tache blanche qui ne paraisse pas
                            plus profonde que la peau, et que le poil ne soit pas devenu blanc, le
                            sacrificateur enfermera pendant sept jours celui qui a la plaie.</dd>

                        <dt>
                            <a name="ch.13.5"></a>13.5
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur l'examinera le septi&egrave;me jour. Si la plaie
                            lui para&icirc;t ne pas avoir fait de progr&egrave;s et ne pas s'&ecirc;tre
                            &eacute;tendue sur la peau, le sacrificateur l'enfermera une seconde fois
                            pendant sept jours.</dd>

                        <dt>
                            <a name="ch.13.6"></a>13.6
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur l'examinera une seconde fois le septi&egrave;me
                            jour. Si la plaie est devenue p&acirc;le et ne s'est pas &eacute;tendue
                            sur la peau, le sacrificateur d&eacute;clarera cet homme pur: c'est une
                            dartre; il lavera ses v&ecirc;tements, et il sera pur.</dd>

                        <dt>
                            <a name="ch.13.7"></a>13.7
                        </dt>

                        <dd>
                            &nbsp;Mais si la dartre s'est &eacute;tendue sur la peau, apr&egrave;s
                            qu'il s'est montr&eacute; au sacrificateur pour &ecirc;tre d&eacute;clar&eacute;
                            pur, il se fera examiner une seconde fois par le sacrificateur.</dd>

                        <dt>
                            <a name="ch.13.8"></a>13.8
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur l'examinera. Si la dartre s'est &eacute;tendue sur
                            la peau, le sacrificateur le d&eacute;clarera impur; c'est la l&egrave;pre.</dd>

                        <dt>
                            <a name="ch.13.9"></a>13.9
                        </dt>

                        <dd>
                            &nbsp;Lorsqu'il y aura sur un homme une plaie de l&egrave;pre, on l'am&egrave;nera
                            au sacrificateur.</dd>

                        <dt>
                            <a name="ch.13.10"></a>13.10
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur l'examinera. S'il y a sur la peau une tumeur blanche,
                            si cette tumeur a fait blanchir le poil, et qu'il y ait une trace de chair
                            vive dans la tumeur,</dd>

                        <dt>
                            <a name="ch.13.11"></a>13.11
                        </dt>

                        <dd>
                            &nbsp;c'est une l&egrave;pre inv&eacute;t&eacute;r&eacute;e dans la peau
                            du corps de cet homme: le sacrificateur le d&eacute;clarera impur; il ne
                            l'enfermera pas, car il est impur.</dd>

                        <dt>
                            <a name="ch.13.12"></a>13.12
                        </dt>

                        <dd>
                            &nbsp;Si la l&egrave;pre fait une &eacute;ruption sur la peau et couvre
                            toute la peau de celui qui a la plaie, depuis la t&ecirc;te jusqu'aux pieds,
                            partout o&ugrave; le sacrificateur portera ses regards, le sacrificateur
                            l'examinera;</dd>

                        <dt>
                            <a name="ch.13.13"></a>13.13
                        </dt>

                        <dd>
                            &nbsp;et quand il aura vu que la l&egrave;pre couvre tout le corps, il
                            d&eacute;clarera pur celui qui a la plaie: comme il est enti&egrave;rement
                            devenu blanc, il est pur.</dd>

                        <dt>
                            <a name="ch.13.14"></a>13.14
                        </dt>

                        <dd>
                            &nbsp;Mais le jour o&ugrave; l'on apercevra en lui de la chair vive, il
                            sera impur;</dd>

                        <dt>
                            <a name="ch.13.15"></a>13.15
                        </dt>

                        <dd>
                            &nbsp;quand le sacrificateur aura vu la chair vive, il le d&eacute;clarera
                            impur: la chair vive est impure, c'est la l&egrave;pre.</dd>

                        <dt>
                            <a name="ch.13.16"></a>13.16
                        </dt>

                        <dd>
                            &nbsp;Si la chair vive change et devient blanche, il ira vers le sacrificateur;</dd>

                        <dt>
                            <a name="ch.13.17"></a>13.17
                        </dt>

                        <dd>
                            &nbsp;le sacrificateur l'examinera, et si la plaie est devenue blanche,
                            le sacrificateur d&eacute;clarera pur celui qui a la plaie: il est pur.</dd>

                        <dt>
                            <a name="ch.13.18"></a>13.18
                        </dt>

                        <dd>
                            &nbsp;Lorsqu'un homme aura eu sur la peau de son corps un ulc&egrave;re
                            qui a &eacute;t&eacute; gu&eacute;ri,</dd>

                        <dt>
                            <a name="ch.13.19"></a>13.19
                        </dt>

                        <dd>
                            &nbsp;et qu'il se manifestera, &agrave; la place o&ugrave; &eacute;tait
                            l'ulc&egrave;re, une tumeur blanche ou une tache d'un blanc rouge&acirc;tre,
                            cet homme se montrera au sacrificateur.</dd>

                        <dt>
                            <a name="ch.13.20"></a>13.20
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur l'examinera. Si la tache para&icirc;t plus enfonc&eacute;e
                            que la peau, et que le poil soit devenu blanc, le sacrificateur le d&eacute;clarera
                            impur: c'est une plaie de l&egrave;pre, qui a fait &eacute;ruption dans
                            l'ulc&egrave;re.</dd>

                        <dt>
                            <a name="ch.13.21"></a>13.21
                        </dt>

                        <dd>
                            &nbsp;Si le sacrificateur voit qu'il n'y a point de poil blanc dans la
                            tache, qu'elle n'est pas plus enfonc&eacute;e que la peau, et qu'elle est
                            devenue p&acirc;le, il enfermera cet homme pendant sept jours.</dd>

                        <dt>
                            <a name="ch.13.22"></a>13.22
                        </dt>

                        <dd>
                            &nbsp;Si la tache s'est &eacute;tendue sur la peau, le sacrificateur le
                            d&eacute;clarera impur: c'est une plaie de l&egrave;pre.</dd>

                        <dt>
                            <a name="ch.13.23"></a>13.23
                        </dt>

                        <dd>
                            &nbsp;Mais si la tache est rest&eacute;e &agrave; la m&ecirc;me place et
                            ne s'est pas &eacute;tendue, c'est une cicatrice de l'ulc&egrave;re: le
                            sacrificateur le d&eacute;clarera pur.</dd>

                        <dt>
                            <a name="ch.13.24"></a>13.24
                        </dt>

                        <dd>
                            &nbsp;Lorsqu'un homme aura eu sur la peau de son corps une br&ucirc;lure
                            par le feu, et qu'il se manifestera sur la trace de la br&ucirc;lure une
                            tache blanche ou d'un blanc rouge&acirc;tre, le sacrificateur l'examinera.</dd>

                        <dt>
                            <a name="ch.13.25"></a>13.25
                        </dt>

                        <dd>
                            &nbsp;Si le poil est devenu blanc dans la tache, et qu'elle paraisse plus
                            profonde que la peau, c'est la l&egrave;pre, qui a fait &eacute;ruption
                            dans la br&ucirc;lure; le sacrificateur d&eacute;clarera cet homme impur:
                            c'est une plaie de l&egrave;pre.</dd>

                        <dt>
                            <a name="ch.13.26"></a>13.26
                        </dt>

                        <dd>
                            &nbsp;Si le sacrificateur voit qu'il n'y a point de poil blanc dans la
                            tache, qu'elle n'est pas plus enfonc&eacute;e que la peau, et qu'elle est
                            devenu p&acirc;le, il enfermera cet homme pendant sept jours.</dd>

                        <dt>
                            <a name="ch.13.27"></a>13.27
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur l'examinera le septi&egrave;me jour. Si la tache
                            s'est &eacute;tendue sur la peau, le sacrificateur le d&eacute;clarera
                            impur: c'est une plaie de l&egrave;pre.</dd>

                        <dt>
                            <a name="ch.13.28"></a>13.28
                        </dt>

                        <dd>
                            &nbsp;Mais si la tache est rest&eacute;e &agrave; la m&ecirc;me place,
                            ne s'est pas &eacute;tendue sur la peau, et est devenue p&acirc;le, c'est
                            la tumeur de la br&ucirc;lure; le sacrificateur le d&eacute;clarera pur,
                            car c'est la cicatrice de la br&ucirc;lure.</dd>

                        <dt>
                            <a name="ch.13.29"></a>13.29
                        </dt>

                        <dd>
                            &nbsp;Lorsqu'un homme ou une femme aura une plaie &agrave; la t&ecirc;te
                            ou &agrave; la barbe,</dd>

                        <dt>
                            <a name="ch.13.30"></a>13.30
                        </dt>

                        <dd>
                            &nbsp;le sacrificateur examinera la plaie. Si elle para&icirc;t plus profonde
                            que la peau, et qu'il y ait du poil jaun&acirc;tre et mince, le sacrificateur
                            d&eacute;clarera cet homme impur: c'est la teigne, c'est la l&egrave;pre
                            de la t&ecirc;te ou de la barbe.</dd>

                        <dt>
                            <a name="ch.13.31"></a>13.31
                        </dt>

                        <dd>
                            &nbsp;Si le sacrificateur voit que la plaie de la teigne ne para&icirc;t
                            pas plus profonde que la peau, et qu'il n'y a point de poil noir, il enferma
                            pendant sept jours celui qui a la plaie de la teigne.</dd>

                        <dt>
                            <a name="ch.13.32"></a>13.32
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur examinera la plaie le septi&egrave;me jour. Si la
                            teigne ne s'est pas &eacute;tendue, s'il n'y a point de poil jaun&acirc;tre,
                            et si elle ne para&icirc;t pas plus profonde que la peau,</dd>

                        <dt>
                            <a name="ch.13.33"></a>13.33
                        </dt>

                        <dd>
                            &nbsp;celui qui a la teigne se rasera, mais il ne rasera point la place
                            o&ugrave; est la teigne; et le sacrificateur l'enfermera une seconde fois
                            pendant sept jours.</dd>

                        <dt>
                            <a name="ch.13.34"></a>13.34
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur examinera la teigne le septi&egrave;me jour. Si
                            la teigne ne s'est pas &eacute;tendue sur la peau, et si elle ne para&icirc;t
                            pas plus profonde que la peau, le sacrificateur le d&eacute;clarera pur;
                            il lavera ses v&ecirc;tements, et il sera pur.</dd>

                        <dt>
                            <a name="ch.13.35"></a>13.35
                        </dt>

                        <dd>
                            &nbsp;Mais si la teigne s'est &eacute;tendue sur la peau, apr&egrave;s
                            qu'il a &eacute;t&eacute; d&eacute;clar&eacute; pur, le sacrificateur l'examinera.</dd>

                        <dt>
                            <a name="ch.13.36"></a>13.36
                        </dt>

                        <dd>
                            &nbsp;Et si la teigne s'est &eacute;tendue sur la peau, le sacrificateur
                            n'aura pas &agrave; rechercher s'il y a du poil jaun&acirc;tre: il est
                            impur.</dd>

                        <dt>
                            <a name="ch.13.37"></a>13.37
                        </dt>

                        <dd>
                            &nbsp;Si la teigne lui para&icirc;t ne pas avoir fait de progr&egrave;s,
                            et qu'il y ait cr&ucirc; du poil noir, la teigne est gu&eacute;rie: il
                            est pur, et le sacrificateur le d&eacute;clarera pur.</dd>

                        <dt>
                            <a name="ch.13.38"></a>13.38
                        </dt>

                        <dd>
                            &nbsp;Lorsqu'un homme ou une femme aura sur la peau de son corps des taches,
                            des taches blanches,</dd>

                        <dt>
                            <a name="ch.13.39"></a>13.39
                        </dt>

                        <dd>
                            &nbsp;le sacrificateur l'examinera. S'il y a sur la peau de son corps des
                            taches d'un blanc p&acirc;le, ce ne sont que des taches qui ont fait &eacute;ruption
                            sur la peau: il est pur.</dd>

                        <dt>
                            <a name="ch.13.40"></a>13.40
                        </dt>

                        <dd>
                            &nbsp;Lorsqu'un homme aura la t&ecirc;te d&eacute;pouill&eacute;e de cheveux,
                            c'est un chauve: il est pur.</dd>

                        <dt>
                            <a name="ch.13.41"></a>13.41
                        </dt>

                        <dd>
                            &nbsp;S'il a la t&ecirc;te d&eacute;pouill&eacute;e de cheveux du c&ocirc;t&eacute;
                            de la face, c'est un chauve par-devant: il est pur.</dd>

                        <dt>
                            <a name="ch.13.42"></a>13.42
                        </dt>

                        <dd>
                            &nbsp;Mais s'il y a dans la partie chauve de devant ou de derri&egrave;re
                            une plaie d'un blanc rouge&acirc;tre, c'est la l&egrave;pre qui a fait
                            &eacute;ruption dans la partie chauve de derri&egrave;re ou de devant.</dd>

                        <dt>
                            <a name="ch.13.43"></a>13.43
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur l'examinera. S'il y a une tumeur de plaie d'un blanc
                            rouge&acirc;tre dans la partie chauve de derri&egrave;re ou de devant,
                            semblable &agrave; la l&egrave;pre sur la peau du corps,</dd>

                        <dt>
                            <a name="ch.13.44"></a>13.44
                        </dt>

                        <dd>
                            &nbsp;c'est un homme l&eacute;preux, il est impur: le sacrificateur le
                            d&eacute;clarera impur; c'est &agrave; la t&ecirc;te qu'est sa plaie.</dd>

                        <dt>
                            <a name="ch.13.45"></a>13.45
                        </dt>

                        <dd>
                            &nbsp;Le l&eacute;preux, atteint de la plaie, portera ses v&ecirc;tements
                            d&eacute;chir&eacute;s, et aura la t&ecirc;te nue; il se couvrira la barbe,
                            et criera: Impur! Impur!</dd>

                        <dt>
                            <a name="ch.13.46"></a>13.46
                        </dt>

                        <dd>
                            &nbsp;Aussi longtemps qu'il aura la plaie, il sera impur: il est impur.
                            Il habitera seul; sa demeure sera hors du camp.</dd>

                        <dt>
                            <a name="ch.13.47"></a>13.47
                        </dt>

                        <dd>
                            &nbsp;Lorsqu'il y aura sur un v&ecirc;tement une plaie de l&egrave;pre,
                            sur un v&ecirc;tement de laine ou sur un v&ecirc;tement de lin,</dd>

                        <dt>
                            <a name="ch.13.48"></a>13.48
                        </dt>

                        <dd>
                            &nbsp;&agrave; la cha&icirc;ne ou &agrave; la trame de lin, ou de laine,
                            sur une peau ou sur quelque ouvrage de peau,</dd>

                        <dt>
                            <a name="ch.13.49"></a>13.49
                        </dt>

                        <dd>
                            &nbsp;et que la plaie sera verd&acirc;tre ou rouge&acirc;tre sur le v&ecirc;tement
                            ou sur la peau, &agrave; la cha&icirc;ne ou &agrave; la trame, ou sur un
                            objet quelconque de peau, c'est une plaie de l&egrave;pre, et elle sera
                            montr&eacute;e au sacrificateur.</dd>

                        <dt>
                            <a name="ch.13.50"></a>13.50
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur examinera la plaie, et il enfermera pendant sept
                            jours ce qui en est attaqu&eacute;.</dd>

                        <dt>
                            <a name="ch.13.51"></a>13.51
                        </dt>

                        <dd>
                            &nbsp;Il examinera la plaie le septi&egrave;me jour. Si la plaie s'est
                            &eacute;tendue sur le v&ecirc;tement, &agrave; la cha&icirc;ne ou &agrave;
                            la trame, sur la peau ou sur l'ouvrage quelconque fait de peau, c'est une
                            plaie de l&egrave;pre inv&eacute;t&eacute;r&eacute;e: l'objet est impur.</dd>

                        <dt>
                            <a name="ch.13.52"></a>13.52
                        </dt>

                        <dd>
                            &nbsp;Il br&ucirc;lera le v&ecirc;tement, la cha&icirc;ne ou la trame de
                            laine ou de lin, l'objet quelconque de peau sur lequel se trouve la plaie,
                            car c'est une l&egrave;pre inv&eacute;t&eacute;r&eacute;e: il sera br&ucirc;l&eacute;
                            au feu.</dd>

                        <dt>
                            <a name="ch.13.53"></a>13.53
                        </dt>

                        <dd>
                            &nbsp;Mais si le sacrificateur voit que la plaie ne s'est pas &eacute;tendue
                            sur le v&ecirc;tement, sur la cha&icirc;ne ou sur la trame, sur l'objet
                            quelconque de peau,</dd>

                        <dt>
                            <a name="ch.13.54"></a>13.54
                        </dt>

                        <dd>
                            &nbsp;il ordonnera qu'on lave ce qui est attaqu&eacute; de la plaie, et
                            il l'enfermera une seconde fois pendant sept jours.</dd>

                        <dt>
                            <a name="ch.13.55"></a>13.55
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur examinera la plaie, apr&egrave;s qu'elle aura &eacute;t&eacute;
                            lav&eacute;e. Si la plaie n'a pas chang&eacute; d'aspect et ne s'est pas
                            &eacute;tendue, l'objet est impur: il sera br&ucirc;l&eacute; au feu; c'est
                            une partie de l'endroit ou de l'envers qui a &eacute;t&eacute; rong&eacute;e.</dd>

                        <dt>
                            <a name="ch.13.56"></a>13.56
                        </dt>

                        <dd>
                            &nbsp;Si le sacrificateur voit que la plaie est devenue p&acirc;le, apr&egrave;s
                            avoir &eacute;t&eacute; lav&eacute;e, il l'arrachera du v&ecirc;tement
                            ou de la peau, de la cha&icirc;ne ou de la trame.</dd>

                        <dt>
                            <a name="ch.13.57"></a>13.57
                        </dt>

                        <dd>
                            &nbsp;Si elle para&icirc;t encore sur le v&ecirc;tement, &agrave; la cha&icirc;ne
                            ou &agrave; la trame, ou sur l'objet quelconque de peau, c'est une &eacute;ruption
                            de l&egrave;pre: ce qui est attaqu&eacute; de la plaie sera br&ucirc;l&eacute;
                            au feu.</dd>

                        <dt>
                            <a name="ch.13.58"></a>13.58
                        </dt>

                        <dd>
                            &nbsp;Le v&ecirc;tement, la cha&icirc;ne ou la trame, l'objet quelconque
                            de peau, qui a &eacute;t&eacute; lav&eacute;, et d'o&ugrave; la plaie a
                            disparu, sera lav&eacute; une seconde fois, et il sera pur.</dd>

                        <dt>
                            <a name="ch.13.59"></a>13.59
                        </dt>

                        <dd>
                            &nbsp;Telle est la loi sur la plaie de la l&egrave;pre, lorsqu'elle attaque
                            les v&ecirc;tements de laine ou de lin, la cha&icirc;ne ou la trame, ou
                            un objet quelconque de peau, et d'apr&egrave;s laquelle ils seront d&eacute;clar&eacute;s
                            purs ou impurs.</dd>
                    </dl>

                    <p style="margin-bottom:20px;padding-top:20px;" class="text-center"><span style="font-size:19px; color:#fdcc52"><b><a name="ch.14"></a>L&eacute;vitique 14</b></span><br><a href="#ch.1">Top</a></p>

                    <dl>
                        <dt>
                            <a name="ch.14.1"></a>14.1
                        </dt>

                        <dd>
                            &nbsp;L'&Eacute;ternel parla &agrave; Mo&iuml;se, et dit:</dd>

                        <dt>
                            <a name="ch.14.2"></a>14.2
                        </dt>

                        <dd>
                            &nbsp;Voici quelle sera la loi sur le l&eacute;preux, pour le jour de sa
                            purification. On l'am&egrave;nera devant le sacrificateur.</dd>

                        <dt>
                            <a name="ch.14.3"></a>14.3
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur sortira du camp, et il examinera le l&eacute;preux.
                            Si le l&eacute;preux est gu&eacute;ri de la plaie de la l&egrave;pre,</dd>

                        <dt>
                            <a name="ch.14.4"></a>14.4
                        </dt>

                        <dd>
                            &nbsp;le sacrificateur ordonnera que l'on prenne, pour celui qui doit &ecirc;tre
                            purifi&eacute;, deux oiseaux vivants et purs, du bois de c&egrave;dre,
                            du cramoisi et de l'hysope.</dd>

                        <dt>
                            <a name="ch.14.5"></a>14.5
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur ordonnera qu'on &eacute;gorge l'un des oiseaux sur
                            un vase de terre, sur de l'eau vive.</dd>

                        <dt>
                            <a name="ch.14.6"></a>14.6
                        </dt>

                        <dd>
                            &nbsp;Il prendra l'oiseau vivant, le bois de c&egrave;dre, le cramoisi
                            et l'hysope; et il les trempera, avec l'oiseau vivant, dans le sang de
                            l'oiseau &eacute;gorg&eacute; sur l'eau vive.</dd>

                        <dt>
                            <a name="ch.14.7"></a>14.7
                        </dt>

                        <dd>
                            &nbsp;Il en fera sept fois l'aspersion sur celui qui doit &ecirc;tre purifi&eacute;
                            de la l&egrave;pre. Puis il le d&eacute;clarera pur, et il l&acirc;chera
                            dans les champs l'oiseau vivant.</dd>

                        <dt>
                            <a name="ch.14.8"></a>14.8
                        </dt>

                        <dd>
                            &nbsp;Celui qui se purifie lavera ses v&ecirc;tements, rasera tout son
                            poil, et se baignera dans l'eau; et il sera pur. Ensuite il pourra entrer
                            dans le camp, mais il restera sept jours hors de sa tente.</dd>

                        <dt>
                            <a name="ch.14.9"></a>14.9
                        </dt>

                        <dd>
                            &nbsp;Le septi&egrave;me jour, il rasera tout son poil, sa t&ecirc;te,
                            sa barbe, ses sourcils, il rasera tout son poil; il lavera ses v&ecirc;tements,
                            et baignera son corps dans l'eau, et il sera pur.</dd>

                        <dt>
                            <a name="ch.14.10"></a>14.10
                        </dt>

                        <dd>
                            &nbsp;Le huiti&egrave;me jour, il prendra deux agneaux sans d&eacute;faut
                            et une brebis d'un an sans d&eacute;faut, trois dixi&egrave;mes d'un &eacute;pha
                            de fleur de farine en offrande p&eacute;trie &agrave; l'huile, et un log
                            d'huile.</dd>

                        <dt>
                            <a name="ch.14.11"></a>14.11
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur qui fait la purification pr&eacute;sentera l'homme
                            qui se purifie et toutes ces choses devant l'&Eacute;ternel, &agrave; l'entr&eacute;e
                            de la tente d'assignation.</dd>

                        <dt>
                            <a name="ch.14.12"></a>14.12
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur prendra l'un des agneaux, et il l'offrira en sacrifice
                            de culpabilit&eacute;, avec le log d'huile; il les agitera de c&ocirc;t&eacute;
                            et d'autre devant l'&Eacute;ternel.</dd>

                        <dt>
                            <a name="ch.14.13"></a>14.13
                        </dt>

                        <dd>
                            &nbsp;Il &eacute;gorgera l'agneau dans le lieu o&ugrave; l'on &eacute;gorge
                            les victimes expiatoires et les holocaustes, dans le lieu saint; car, dans
                            le sacrifice de culpabilit&eacute;, comme dans le sacrifice d'expiation,
                            la victime est pour le sacrificateur; c'est une chose tr&egrave;s sainte.</dd>

                        <dt>
                            <a name="ch.14.14"></a>14.14
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur prendra du sang de la victime de culpabilit&eacute;;
                            il en mettra sur le lobe de l'oreille droite de celui qui se purifie, sur
                            le pouce de sa main droite et sur le gros orteil de son pied droit.</dd>

                        <dt>
                            <a name="ch.14.15"></a>14.15
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur prendra du log d'huile, et il en versera dans le
                            creux de sa main gauche.</dd>

                        <dt>
                            <a name="ch.14.16"></a>14.16
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur trempera le doigt de sa main droite dans l'huile
                            qui est dans le creux de sa main gauche, et il fera avec le doigt sept
                            fois l'aspersion de l'huile devant l'&Eacute;ternel.</dd>

                        <dt>
                            <a name="ch.14.17"></a>14.17
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur mettra de l'huile qui lui reste dans la main sur
                            le lobe de l'oreille droite de celui qui se purifie, sur le pouce de sa
                            main droite et sur le gros orteil de son pied droit, par-dessus le sang
                            de la victime de culpabilit&eacute;.</dd>

                        <dt>
                            <a name="ch.14.18"></a>14.18
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur mettra ce qui lui reste d'huile dans la main sur
                            la t&ecirc;te de celui qui se purifie; et le sacrificateur fera pour lui
                            l'expiation devant l'&Eacute;ternel.</dd>

                        <dt>
                            <a name="ch.14.19"></a>14.19
                        </dt>

                        <dd>
                            &nbsp;Puis le sacrificateur offrira le sacrifice d'expiation; et il fera
                            l'expiation pour celui qui se purifie de sa souillure.</dd>

                        <dt>
                            <a name="ch.14.20"></a>14.20
                        </dt>

                        <dd>
                            &nbsp;Ensuite il &eacute;gorgera l'holocauste. Le sacrificateur offrira
                            sur l'autel l'holocauste et l'offrande; et il fera pour cet homme l'expiation,
                            et il sera pur.</dd>

                        <dt>
                            <a name="ch.14.21"></a>14.21
                        </dt>

                        <dd>
                            &nbsp;S'il est pauvre et que ses ressources soient insuffisantes, il prendra
                            un seul agneau, qui sera offert en sacrifice de culpabilit&eacute;, apr&egrave;s
                            avoir &eacute;t&eacute; agit&eacute; de c&ocirc;t&eacute; et d'autre, et
                            avec lequel on fera pour lui l'expiation. Il prendra un seul dixi&egrave;me
                            de fleur de farine p&eacute;trie &agrave; l'huile pour l'offrande, et un
                            log d'huile.</dd>

                        <dt>
                            <a name="ch.14.22"></a>14.22
                        </dt>

                        <dd>
                            &nbsp;Il prendra aussi deux tourterelles ou deux jeunes pigeons, selon
                            ses ressources, l'un pour le sacrifice d'expiation, l'autre pour l'holocauste.</dd>

                        <dt>
                            <a name="ch.14.23"></a>14.23
                        </dt>

                        <dd>
                            &nbsp;Le huiti&egrave;me jour, il apportera pour sa purification toutes
                            ces choses au sacrificateur, &agrave; l'entr&eacute;e de la tente d'assignation,
                            devant l'&Eacute;ternel.</dd>

                        <dt>
                            <a name="ch.14.24"></a>14.24
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur prendra l'agneau pour le sacrifice de culpabilit&eacute;,
                            et le log d'huile; et il les agitera de c&ocirc;t&eacute; et d'autre devant
                            l'&Eacute;ternel.</dd>

                        <dt>
                            <a name="ch.14.25"></a>14.25
                        </dt>

                        <dd>
                            &nbsp;Il &eacute;gorgera l'agneau du sacrifice de culpabilit&eacute;. Le
                            sacrificateur prendra du sang de la victime de culpabilit&eacute;; il en
                            mettra sur le lobe de l'oreille droite de celui qui se purifie, sur le
                            pouce de sa main droite et sur le gros orteil de son pied droit.</dd>

                        <dt>
                            <a name="ch.14.26"></a>14.26
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur versera de l'huile dans le creux de sa main gauche.</dd>

                        <dt>
                            <a name="ch.14.27"></a>14.27
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur fera avec le doigt de sa main droite sept fois l'aspersion
                            de l'huile qui est dans sa main gauche, devant l'&Eacute;ternel.</dd>

                        <dt>
                            <a name="ch.14.28"></a>14.28
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur mettra de l'huile qui est dans sa main sur le lobe
                            de l'oreille droite de celui qui se purifie, sur le pouce de sa main droite
                            et sur le gros orteil de son pied droit, &agrave; la place o&ugrave; il
                            a mis du sang de la victime de culpabilit&eacute;.</dd>

                        <dt>
                            <a name="ch.14.29"></a>14.29
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur mettra ce qui lui reste d'huile dans la main sur
                            la t&ecirc;te de celui qui se purifie, afin de faire pour lui l'expiation
                            devant l'&Eacute;ternel.</dd>

                        <dt>
                            <a name="ch.14.30"></a>14.30
                        </dt>

                        <dd>
                            &nbsp;Puis il offrira l'une des tourterelles ou l'un des jeunes pigeons
                            qu'il a pu se procurer,</dd>

                        <dt>
                            <a name="ch.14.31"></a>14.31
                        </dt>

                        <dd>
                            &nbsp;l'un en sacrifice d'expiation, l'autre en holocauste, avec l'offrande;
                            et le sacrificateur fera pour celui qui se purifie l'expiation devant l'&Eacute;ternel.</dd>

                        <dt>
                            <a name="ch.14.32"></a>14.32
                        </dt>

                        <dd>
                            &nbsp;Telle est la loi pour la purification de celui qui a une plaie de
                            l&egrave;pre, et dont les ressources sont insuffisantes.</dd>

                        <dt>
                            <a name="ch.14.33"></a>14.33
                        </dt>

                        <dd>
                            &nbsp;L'&Eacute;ternel parla &agrave; Mo&iuml;se et &agrave; Aaron, et
                            dit:</dd>

                        <dt>
                            <a name="ch.14.34"></a>14.34
                        </dt>

                        <dd>
                            &nbsp;Lorsque vous serez entr&eacute;s dans le pays de Canaan, dont je
                            vous donne la possession; si je mets une plaie de l&egrave;pre sur une
                            maison du pays que vous poss&eacute;derez,</dd>

                        <dt>
                            <a name="ch.14.35"></a>14.35
                        </dt>

                        <dd>
                            &nbsp;celui &agrave; qui appartiendra la maison ira le d&eacute;clarer
                            au sacrificateur, et dira: J'aper&ccedil;ois comme une plaie dans ma maison.</dd>

                        <dt>
                            <a name="ch.14.36"></a>14.36
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur, avant d'y entrer pour examiner la plaie, ordonnera
                            qu'on vide la maison, afin que tout ce qui y est ne devienne pas impur.
                            Apr&egrave;s cela, le sacrificateur entrera pour examiner la maison.</dd>

                        <dt>
                            <a name="ch.14.37"></a>14.37
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur examinera la plaie. S'il voit qu'elle offre sur
                            les murs de la maison des cavit&eacute;s verd&acirc;tres ou rouge&acirc;tres,
                            paraissant plus enfonc&eacute;es que le mur,</dd>

                        <dt>
                            <a name="ch.14.38"></a>14.38
                        </dt>

                        <dd>
                            &nbsp;il sortira de la maison, et, quand il sera &agrave; la porte, il
                            fera fermer la maison pour sept jours.</dd>

                        <dt>
                            <a name="ch.14.39"></a>14.39
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur y retournera le septi&egrave;me jour. S'il voit
                            que la plaie s'est &eacute;tendue sur les murs de la maison,</dd>

                        <dt>
                            <a name="ch.14.40"></a>14.40
                        </dt>

                        <dd>
                            &nbsp;il ordonnera qu'on &ocirc;te les pierres attaqu&eacute;es de la plaie,
                            et qu'on les jette hors de la ville, dans un lieu impur.</dd>

                        <dt>
                            <a name="ch.14.41"></a>14.41
                        </dt>

                        <dd>
                            &nbsp;Il fera r&acirc;cler tout l'int&eacute;rieur de la maison; et l'on
                            jettera hors de la ville, dans un lieu impur, la poussi&egrave;re qu'on
                            aura r&acirc;cl&eacute;e.</dd>

                        <dt>
                            <a name="ch.14.42"></a>14.42
                        </dt>

                        <dd>
                            &nbsp;On prendra d'autres pierres, que l'on mettra &agrave; la place des
                            premi&egrave;res; et l'on prendra d'autre mortier, pour recr&eacute;pir
                            la maison.</dd>

                        <dt>
                            <a name="ch.14.43"></a>14.43
                        </dt>

                        <dd>
                            &nbsp;Si la plaie revient et fait &eacute;ruption dans la maison, apr&egrave;s
                            qu'on a &ocirc;t&eacute; les pierres, r&acirc;cl&eacute; et recr&eacute;pi
                            la maison,</dd>

                        <dt>
                            <a name="ch.14.44"></a>14.44
                        </dt>

                        <dd>
                            &nbsp;le sacrificateur y retournera. S'il voit que la plaie s'est &eacute;tendue
                            dans la maison, c'est une l&egrave;pre inv&eacute;t&eacute;r&eacute;e dans
                            la maison: elle est impure.</dd>

                        <dt>
                            <a name="ch.14.45"></a>14.45
                        </dt>

                        <dd>
                            &nbsp;On abattra la maison, les pierres, le bois, et tout le mortier de
                            la maison; et l'on portera ces choses hors de la ville dans un lieu impur.</dd>

                        <dt>
                            <a name="ch.14.46"></a>14.46
                        </dt>

                        <dd>
                            &nbsp;Celui qui sera entr&eacute; dans la maison pendant tout le temps
                            qu'elle &eacute;tait ferm&eacute;e sera impur jusqu'au soir.</dd>

                        <dt>
                            <a name="ch.14.47"></a>14.47
                        </dt>

                        <dd>
                            &nbsp;Celui qui aura couch&eacute; dans la maison lavera ses v&ecirc;tements.
                            Celui qui aura mang&eacute; dans la maison lavera aussi ses v&ecirc;tements.</dd>

                        <dt>
                            <a name="ch.14.48"></a>14.48
                        </dt>

                        <dd>
                            &nbsp;Si le sacrificateur, qui est retourn&eacute; dans la maison, voit
                            que la plaie ne s'est pas &eacute;tendue, apr&egrave;s que la maison a
                            &eacute;t&eacute; recr&eacute;pie, il d&eacute;clarera la maison pure,
                            car la plaie est gu&eacute;rie.</dd>

                        <dt>
                            <a name="ch.14.49"></a>14.49
                        </dt>

                        <dd>
                            &nbsp;Il prendra, pour purifier la maison, deux oiseaux, du bois de c&egrave;dre,
                            du cramoisi et de l'hysope.</dd>

                        <dt>
                            <a name="ch.14.50"></a>14.50
                        </dt>

                        <dd>
                            &nbsp;Il &eacute;gorgera l'un des oiseaux sur un vase de terre, sur de
                            l'eau vive.</dd>

                        <dt>
                            <a name="ch.14.51"></a>14.51
                        </dt>

                        <dd>
                            &nbsp;Il prendra le bois de c&egrave;dre, l'hysope, le cramoisi et l'oiseau
                            vivant; il les trempera dans le sang de l'oiseau &eacute;gorg&eacute; et
                            dans l'eau vive, et il en fera sept fois l'aspersion sur la maison.</dd>

                        <dt>
                            <a name="ch.14.52"></a>14.52
                        </dt>

                        <dd>
                            &nbsp;Il purifiera la maison avec le sang de l'oiseau, avec de l'eau vive,
                            avec l'oiseau vivant, avec le bois de c&egrave;dre, l'hysope et le cramoisi.</dd>

                        <dt>
                            <a name="ch.14.53"></a>14.53
                        </dt>

                        <dd>
                            &nbsp;Il l&acirc;chera l'oiseau vivant hors de la ville, dans les champs.
                            C'est ainsi qu'il fera pour la maison l'expiation, et elle sera pure.</dd>

                        <dt>
                            <a name="ch.14.54"></a>14.54
                        </dt>

                        <dd>
                            &nbsp;Telle est la loi pour toute plaie de l&egrave;pre et pour la teigne,</dd>

                        <dt>
                            <a name="ch.14.55"></a>14.55
                        </dt>

                        <dd>
                            &nbsp;pour la l&egrave;pre des v&ecirc;tements et des maisons,</dd>

                        <dt>
                            <a name="ch.14.56"></a>14.56
                        </dt>

                        <dd>
                            &nbsp;pour les tumeurs, les dartres et les taches:</dd>

                        <dt>
                            <a name="ch.14.57"></a>14.57
                        </dt>

                        <dd>
                            &nbsp;elle enseigne quand une chose est impure, et quand elle est pure.
                            Telle est la loi sur la l&egrave;pre.</dd>

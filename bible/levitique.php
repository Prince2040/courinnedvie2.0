                            &nbsp;Si tu as &agrave; faire l'estimation d'un m&acirc;le de vingt &agrave;
                            soixante ans, ton estimation sera de cinquante sicles d'argent, selon le
                            sicle du sanctuaire;</dd>

                        <dt>
                            <a name="ch.27.4"></a>27.4
                        </dt>

                        <dd>
                            &nbsp;si c'est une femme, ton estimation sera de trente sicles.</dd>

                        <dt>
                            <a name="ch.27.5"></a>27.5
                        </dt>

                        <dd>
                            &nbsp;De cinq &agrave; vingt ans, ton estimation sera de vingt sicles pour
                            un m&acirc;le, et de dix sicles pour une fille.</dd>

                        <dt>
                            <a name="ch.27.6"></a>27.6
                        </dt>

                        <dd>
                            &nbsp;D'un mois &agrave; cinq ans, ton estimation sera de cinq sicles d'argent
                            pour un m&acirc;le, et de trois sicles d'argent pour une fille.</dd>

                        <dt>
                            <a name="ch.27.7"></a>27.7
                        </dt>

                        <dd>
                            &nbsp;De soixante ans et au-dessus, ton estimation sera de quinze sicles
                            pour un m&acirc;le, et de dix sicles pour une femme.</dd>

                        <dt>
                            <a name="ch.27.8"></a>27.8
                        </dt>

                        <dd>
                            &nbsp;Si celui qui a fait le voeu est trop pauvre pour payer ton estimation,
                            on le pr&eacute;sentera au sacrificateur, qui le taxera, et le sacrificateur
                            fera une estimation en rapport avec les ressources de cet homme.</dd>

                        <dt>
                            <a name="ch.27.9"></a>27.9
                        </dt>

                        <dd>
                            &nbsp;S'il s'agit d'animaux qui peuvent &ecirc;tre offerts en sacrifice
                            &agrave; l'&Eacute;ternel, tout animal qu'on donnera &agrave; l'&Eacute;ternel
                            sera chose sainte.</dd>

                        <dt>
                            <a name="ch.27.10"></a>27.10
                        </dt>

                        <dd>
                            &nbsp;On ne le changera point, et l'on n'en mettra point un mauvais &agrave;
                            la place d'un bon ni un bon &agrave; la place d'un mauvais; si l'on remplace
                            un animal par un autre, ils seront l'un et l'autre chose sainte.</dd>

                        <dt>
                            <a name="ch.27.11"></a>27.11
                        </dt>

                        <dd>
                            &nbsp;S'il s'agit d'animaux impurs, qui ne peuvent &ecirc;tre offerts en
                            sacrifice &agrave; l'&Eacute;ternel, on pr&eacute;sentera l'animal au sacrificateur,</dd>

                        <dt>
                            <a name="ch.27.12"></a>27.12
                        </dt>

                        <dd>
                            &nbsp;qui en fera l'estimation selon qu'il sera bon ou mauvais, et l'on
                            s'en rapportera &agrave; l'estimation du sacrificateur.</dd>

                        <dt>
                            <a name="ch.27.13"></a>27.13
                        </dt>

                        <dd>
                            &nbsp;Si on veut le racheter, on ajoutera un cinqui&egrave;me &agrave;
                            son estimation.</dd>

                        <dt>
                            <a name="ch.27.14"></a>27.14
                        </dt>

                        <dd>
                            &nbsp;Si quelqu'un sanctifie sa maison en la consacrant &agrave; l'&Eacute;ternel,
                            le sacrificateur en fera l'estimation selon qu'elle sera bonne ou mauvaise,
                            et l'on s'en tiendra &agrave; l'estimation du sacrificateur.</dd>

                        <dt>
                            <a name="ch.27.15"></a>27.15
                        </dt>

                        <dd>
                            &nbsp;Si celui qui a sanctifi&eacute; sa maison veut la racheter, il ajoutera
                            un cinqui&egrave;me au prix de son estimation, et elle sera &agrave; lui.</dd>

                        <dt>
                            <a name="ch.27.16"></a>27.16
                        </dt>

                        <dd>
                            &nbsp;Si quelqu'un sanctifie &agrave; l'&Eacute;ternel un champ de sa propri&eacute;t&eacute;,
                            ton estimation sera en rapport avec la quantit&eacute; de semence, cinquante
                            sicles d'argent pour un homer de semence d'orge.</dd>

                        <dt>
                            <a name="ch.27.17"></a>27.17
                        </dt>

                        <dd>
                            &nbsp;Si c'est d&egrave;s l'ann&eacute;e du jubil&eacute; qu'il sanctifie
                            son champ, on s'en tiendra &agrave; ton estimation;</dd>

                        <dt>
                            <a name="ch.27.18"></a>27.18
                        </dt>

                        <dd>
                            &nbsp;si c'est apr&egrave;s le jubil&eacute; qu'il sanctifie son champ,
                            le sacrificateur en &eacute;valuera le prix &agrave; raison du nombre d'ann&eacute;es
                            qui restent jusqu'au jubil&eacute;, et il sera fait une r&eacute;duction
                            sur ton estimation.</dd>

                        <dt>
                            <a name="ch.27.19"></a>27.19
                        </dt>

                        <dd>
                            &nbsp;Si celui qui a sanctifi&eacute; son champ veut le racheter, il ajoutera
                            un cinqui&egrave;me au prix de ton estimation, et le champ lui restera.</dd>

                        <dt>
                            <a name="ch.27.20"></a>27.20
                        </dt>

                        <dd>
                            &nbsp;S'il ne rach&egrave;te point le champ, et qu'on le vende &agrave;
                            un autre homme, il ne pourra plus &ecirc;tre rachet&eacute;.</dd>

                        <dt>
                            <a name="ch.27.21"></a>27.21
                        </dt>

                        <dd>
                            &nbsp;Et quand l'acqu&eacute;reur en sortira au jubil&eacute;, ce champ
                            sera consacr&eacute; &agrave; l'&Eacute;ternel, comme un champ qui a &eacute;t&eacute;
                            d&eacute;vou&eacute;; il deviendra la propri&eacute;t&eacute; du sacrificateur.</dd>

                        <dt>
                            <a name="ch.27.22"></a>27.22
                        </dt>

                        <dd>
                            &nbsp;Si quelqu'un sanctifie &agrave; l'&Eacute;ternel un champ qu'il a
                            acquis et qui ne fait point partie de sa propri&eacute;t&eacute;,</dd>

                        <dt>
                            <a name="ch.27.23"></a>27.23
                        </dt>

                        <dd>
                            &nbsp;le sacrificateur en &eacute;valuera le prix d'apr&egrave;s ton estimation
                            jusqu'&agrave; l'ann&eacute;e du jubil&eacute;, et cet homme paiera le
                            jour m&ecirc;me le prix fix&eacute;, comme &eacute;tant consacr&eacute;
                            &agrave; l'&Eacute;ternel.</dd>

                        <dt>
                            <a name="ch.27.24"></a>27.24
                        </dt>

                        <dd>
                            &nbsp;L'ann&eacute;e du jubil&eacute;, le champ retournera &agrave; celui
                            de qui il avait &eacute;t&eacute; achet&eacute; et de la propri&eacute;t&eacute;
                            dont il faisait partie.</dd>

                        <dt>
                            <a name="ch.27.25"></a>27.25
                        </dt>

                        <dd>
                            &nbsp;Toutes tes estimations se feront en sicles du sanctuaire: le sicle
                            est de vingt gu&eacute;ras.</dd>

                        <dt>
                            <a name="ch.27.26"></a>27.26
                        </dt>

                        <dd>
                            &nbsp;Nul ne pourra sanctifier le premier-n&eacute; de son b&eacute;tail,
                            lequel appartient d&eacute;j&agrave;agrave; &agrave; l'&Eacute;ternel en sa qualit&eacute;
                            de premier-n&eacute;; soit boeuf, soit agneau, il appartient &agrave; l'&Eacute;ternel.</dd>

                        <dt>
                            <a name="ch.27.27"></a>27.27
                        </dt>

                        <dd>
                            &nbsp;S'il s'agit d'un animal impur, on le rach&egrave;tera au prix de
                            ton estimation, en y ajoutant un cinqui&egrave;me; s'il n'est pas rachet&eacute;,
                            il sera vendu d'apr&egrave;s ton estimation.</dd>

                        <dt>
                            <a name="ch.27.28"></a>27.28
                        </dt>

                        <dd>
                            &nbsp;Tout ce qu'un homme d&eacute;vouera par interdit &agrave; l'&Eacute;ternel,
                            dans ce qui lui appartient, ne pourra ni se vendre, ni se racheter, que
                            ce soit une personne, un animal, ou un champ de sa propri&eacute;t&eacute;;
                            tout ce qui sera d&eacute;vou&eacute; par interdit sera enti&egrave;rement
                            consacr&eacute; &agrave; l'&Eacute;ternel.</dd>

                        <dt>
                            <a name="ch.27.29"></a>27.29
                        </dt>

                        <dd>
                            &nbsp;Aucune personne d&eacute;vou&eacute;e par interdit ne pourra &ecirc;tre
                            rachet&eacute;e, elle sera mise &agrave; mort.</dd>

                        <dt>
                            <a name="ch.27.30"></a>27.30
                        </dt>

                        <dd>
                            &nbsp;Toute d&icirc;me de la terre, soit des r&eacute;coltes de la terre,
                            soit du fruit des arbres, appartient &agrave; l'&Eacute;ternel; c'est une
                            chose consacr&eacute;e &agrave; l'&Eacute;ternel.</dd>

                        <dt>
                            <a name="ch.27.31"></a>27.31
                        </dt>

                        <dd>
                            &nbsp;Si quelqu'un veut racheter quelque chose de sa d&icirc;me, il y ajoutera
                            un cinqui&egrave;me.</dd>

                        <dt>
                            <a name="ch.27.32"></a>27.32
                        </dt>

                        <dd>
                            &nbsp;Toute d&icirc;me de gros et de menu b&eacute;tail, de tout ce qui
                            passe sous la houlette, sera une d&icirc;me consacr&eacute;e &agrave; l'&Eacute;ternel.</dd>

                        <dt>
                            <a name="ch.27.33"></a>27.33
                        </dt>

                        <dd>
                            &nbsp;On n'examinera point si l'animal est bon ou mauvais, et l'on ne fera
                            point d'&eacute;change; si l'on remplace un animal par un autre, ils seront
                            l'un et l'autre chose sainte, et ne pourront &ecirc;tre rachet&eacute;s.</dd>

                        <dt>
                            <a name="ch.27.34"></a>27.34
                        </dt>

                        <dd>
                            &nbsp;Tels sont les commandements que l'&Eacute;ternel donna &agrave; Mo&iuml;se
                            pour les enfants d'Isra&euml;l, sur la montagne de Sina&iuml;.</dd>
                    </dl>


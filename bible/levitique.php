                            <a name="ch.15.12"></a>15.12
                        </dt>

                        <dd>
                            &nbsp;Tout vase de terre qui sera touch&eacute; par lui sera bris&eacute;,
                            et tout vase de bois sera lav&eacute; dans l'eau.</dd>


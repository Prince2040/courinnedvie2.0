                            mais qui ne rumine pas: vous le regarderez comme impur.</dd>

                        <dt>
                            <a name="ch.11.8"></a>11.8
                        </dt>

                        <dd>
                            &nbsp;Vous ne mangerez pas de leur chair, et vous ne toucherez pas leurs
                            corps morts: vous les regarderez comme impurs.</dd>

                        <dt>
                            <a name="ch.11.9"></a>11.9
                        </dt>

                        <dd>
                            &nbsp;Voici les animaux dont vous mangerez parmi tous ceux qui sont dans
                            les eaux. Vous mangerez de tous ceux qui ont des nageoires et des &eacute;cailles,
                            et qui sont dans les eaux, soit dans les mers, soit dans les rivi&egrave;res.</dd>

                        <dt>
                            <a name="ch.11.10"></a>11.10
                        </dt>

                        <dd>
                            &nbsp;Mais vous aurez en abomination tous ceux qui n'ont pas des nageoires
                            et des &eacute;cailles, parmi tout ce qui se meut dans les eaux et tout
                            ce qui est vivant dans les eaux, soit dans les mers, soit dans les rivi&egrave;res.</dd>

                        <dt>
                            <a name="ch.11.11"></a>11.11
                        </dt>

                        <dd>
                            &nbsp;Vous les aurez en abomination, vous ne mangerez pas de leur chair,
                            et vous aurez en abomination leurs corps morts.</dd>

                        <dt>
                            <a name="ch.11.12"></a>11.12
                        </dt>

                        <dd>
                            &nbsp;Vous aurez en abomination tous ceux qui, dans les eaux, n'ont pas
                            des nageoires et des &eacute;cailles.</dd>

                        <dt>
                            <a name="ch.11.13"></a>11.13
                        </dt>

                        <dd>
                            &nbsp;Voici, parmi les oiseaux, ceux que vous aurez en abomination, et
                            dont on ne mangera pas: l'aigle, l'orfraie et l'aigle de mer;</dd>

                        <dt>
                            <a name="ch.11.14"></a>11.14
                        </dt>

                        <dd>
                            &nbsp;le milan, l'autour et ce qui est de son esp&egrave;ce;</dd>

                        <dt>
                            <a name="ch.11.15"></a>11.15
                        </dt>

                        <dd>
                            &nbsp;le corbeau et toutes ses esp&egrave;ces;</dd>

                        <dt>
                            <a name="ch.11.16"></a>11.16
                        </dt>

                        <dd>
                            &nbsp;l'autruche, le hibou, la mouette, l'&eacute;pervier et ce qui est
                            de son esp&egrave;ce;</dd>

                        <dt>
                            <a name="ch.11.17"></a>11.17
                        </dt>

                        <dd>
                            &nbsp;le chat-huant, le plongeon et la chouette;</dd>

                        <dt>
                            <a name="ch.11.18"></a>11.18
                        </dt>

                        <dd>
                            &nbsp;le cygne, le p&eacute;lican et le cormoran;</dd>

                        <dt>
                            <a name="ch.11.19"></a>11.19
                        </dt>

                        <dd>
                            &nbsp;la cigogne, le h&eacute;ron et ce qui est de son esp&egrave;ce, la
                            huppe et la chauve-souris.</dd>

                        <dt>
                            <a name="ch.11.20"></a>11.20
                        </dt>

                        <dd>
                            &nbsp;Vous aurez en abomination tout reptile qui vole et qui marche sur
                            quatre pieds.</dd>

                        <dt>
                            <a name="ch.11.21"></a>11.21
                        </dt>

                        <dd>
                            &nbsp;Mais, parmi tous les reptiles qui volent et qui marchent sur quatre
                            pieds, vous mangerez ceux qui ont des jambes au-dessus de leurs pieds,
                            pour sauter sur la terre.</dd>

                        <dt>
                            <a name="ch.11.22"></a>11.22
                        </dt>

                        <dd>
                            &nbsp;Voici ceux que vous mangerez: la sauterelle, le solam, le hargol
                            et le hagab, selon leurs esp&egrave;ces.</dd>

                        <dt>
                            <a name="ch.11.23"></a>11.23
                        </dt>

                        <dd>
                            &nbsp;Vous aurez en abomination tous les autres reptiles qui volent et
                            qui ont quatre pieds.</dd>

                        <dt>
                            <a name="ch.11.24"></a>11.24
                        </dt>

                        <dd>
                            &nbsp;Ils vous rendront impurs: quiconque touchera leurs corps morts sera
                            impur jusqu'au soir,</dd>

                        <dt>
                            <a name="ch.11.25"></a>11.25
                        </dt>

                        <dd>
                            &nbsp;et quiconque portera leurs corps morts lavera ses v&ecirc;tements
                            et sera impur jusqu'au soir.</dd>

                        <dt>
                            <a name="ch.11.26"></a>11.26
                        </dt>

                        <dd>
                            &nbsp;Vous regarderez comme impur tout animal qui a la corne fendue, mais
                            qui n'a pas le pied fourchu et qui ne rumine pas: quiconque le touchera
                            sera impur.</dd>

                        <dt>
                            <a name="ch.11.27"></a>11.27
                        </dt>

                        <dd>
                            &nbsp;Vous regarderez comme impurs tous ceux des animaux &agrave; quatre
                            pieds qui marchent sur leurs pattes: quiconque touchera leurs corps morts
                            sera impur jusqu'au soir,</dd>

                        <dt>
                            <a name="ch.11.28"></a>11.28
                        </dt>

                        <dd>
                            &nbsp;et quiconque portera leurs corps morts lavera ses v&ecirc;tements
                            et sera impur jusqu'au soir. Vous les regarderez comme impurs.</dd>

                        <dt>
                            <a name="ch.11.29"></a>11.29
                        </dt>

                        <dd>
                            &nbsp;Voici, parmi les animaux qui rampent sur la terre, ceux que vous
                            regarderez comme impurs: la taupe, la souris et le l&eacute;zard, selon
                            leurs esp&egrave;ces;</dd>

                        <dt>
                            <a name="ch.11.30"></a>11.30
                        </dt>

                        <dd>
                            &nbsp;le h&eacute;risson, la grenouille, la tortue, le lima&ccedil;on et
                            le cam&eacute;l&eacute;on.</dd>

                        <dt>
                            <a name="ch.11.31"></a>11.31
                        </dt>

                        <dd>
                            &nbsp;Vous les regarderez comme impurs parmi tous les reptiles: quiconque
                            les touchera morts sera impur jusqu'au soir.</dd>

                        <dt>
                            <a name="ch.11.32"></a>11.32
                        </dt>

                        <dd>
                            &nbsp;Tout objet sur lequel tombera quelque chose de leurs corps morts
                            sera souill&eacute;, ustensiles de bois, v&ecirc;tement, peau, sac, tout
                            objet dont on fait usage; il sera mis dans l'eau, et restera souill&eacute;
                            jusqu'au soir; apr&egrave;s quoi, il sera pur.</dd>

                        <dt>
                            <a name="ch.11.33"></a>11.33
                        </dt>

                        <dd>
                            &nbsp;Tout ce qui se trouvera dans un vase de terre o&ugrave; il en tombera
                            quelque chose, sera souill&eacute;, et vous briserez le vase.</dd>

                        <dt>
                            <a name="ch.11.34"></a>11.34
                        </dt>

                        <dd>
                            &nbsp;Tout aliment qui sert &agrave; la nourriture, et sur lequel il sera
                            tomb&eacute; de cette eau, sera souill&eacute;; et toute boisson dont on
                            fait usage, quel que soit le vase qui la contienne, sera souill&eacute;e.</dd>

                        <dt>
                            <a name="ch.11.35"></a>11.35
                        </dt>

                        <dd>
                            &nbsp;Tout objet sur lequel tombera quelque chose de leurs corps morts
                            sera souill&eacute;; le four et le foyer seront d&eacute;truits: ils seront
                            souill&eacute;s, et vous les regarderez comme souill&eacute;s.</dd>

                        <dt>
                            <a name="ch.11.36"></a>11.36
                        </dt>

                        <dd>
                            &nbsp;Il n'y aura que les sources et les citernes, formant des amas d'eaux,
                            qui resteront pures; mais celui qui y touchera de leurs corps morts sera
                            impur.</dd>

                        <dt>
                            <a name="ch.11.37"></a>11.37
                        </dt>

                        <dd>
                            &nbsp;S'il tombe quelque chose de leurs corps morts sur une semence qui
                            doit &ecirc;tre sem&eacute;e, elle restera pure;</dd>

                        <dt>
                            <a name="ch.11.38"></a>11.38
                        </dt>

                        <dd>
                            &nbsp;mais si l'on a mis de l'eau sur la semence, et qu'il y tombe quelque
                            chose de leurs corps morts, vous la regarderez comme souill&eacute;e.</dd>

                        <dt>
                            <a name="ch.11.39"></a>11.39
                        </dt>

                        <dd>
                            &nbsp;S'il meurt un des animaux qui vous servent de nourriture, celui qui
                            touchera son corps mort sera impur jusqu'au soir;</dd>

                        <dt>
                            <a name="ch.11.40"></a>11.40
                        </dt>

                        <dd>
                            &nbsp;celui qui mangera de son corps mort lavera ses v&ecirc;tements et
                            sera impur jusqu'au soir, et celui qui portera son corps mort lavera ses
                            v&ecirc;tements et sera impur jusqu'au soir.</dd>

                        <dt>
                            <a name="ch.11.41"></a>11.41
                        </dt>

                        <dd>
                            &nbsp;Vous aurez en abomination tout reptile qui rampe sur la terre: on
                            n'en mangera point.</dd>

                        <dt>
                            <a name="ch.11.42"></a>11.42
                        </dt>

                        <dd>
                            &nbsp;Vous ne mangerez point, parmi tous les reptiles qui rampent sur la
                            terre, de tous ceux qui se tra&icirc;nent sur le ventre, ni de tous ceux
                            qui marchent sur quatre pieds ou sur un grand nombre de pieds; car vous
                            les aurez en abomination.</dd>

                        <dt>
                            <a name="ch.11.43"></a>11.43
                        </dt>

                        <dd>
                            &nbsp;Ne rendez point vos personnes abominables par tous ces reptiles qui
                            rampent; ne vous rendez point impurs par eux, ne vous souillez point par
                            eux.</dd>

                        <dt>
                            <a name="ch.11.44"></a>11.44
                        </dt>

                        <dd>
                            &nbsp;Car je suis l'&Eacute;ternel, votre Dieu; vous vous sanctifierez,
                            et vous serez saints, car je suis saint; et vous ne vous rendrez point
                            impurs par tous ces reptiles qui rampent sur la terre.</dd>

                        <dt>
                            <a name="ch.11.45"></a>11.45
                        </dt>

                        <dd>
                            &nbsp;Car je suis l'&Eacute;ternel, qui vous ai fait monter du pays d'&Eacute;gypte,
                            pour &ecirc;tre votre Dieu, et pour que vous soyez saints; car je suis
                            saint.</dd>

                        <dt>
                            <a name="ch.11.46"></a>11.46
                        </dt>

                        <dd>
                            &nbsp;Telle est la loi touchant les animaux, les oiseaux, tous les &ecirc;tres
                            vivants qui se meuvent dans les eaux, et tous les &ecirc;tres qui rampent
                            sur la terre,</dd>

                        <dt>
                            <a name="ch.11.47"></a>11.47
                        </dt>

                        <dd>
                            &nbsp;afin que vous distinguiez ce qui est impur et ce qui est pur, l'animal
                            qui se mange et l'animal qui ne se mange pas.</dd>
                    </dl>


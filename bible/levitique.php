                        <dt>
                            <a name="ch.2.7"></a>2.7
                        </dt>

                        <dd>
                            &nbsp;Si ton offrande est un g&acirc;teau cuit sur le gril, il sera fait
                            de fleur de farine p&eacute;trie &agrave; l'huile.</dd>

                        <dt>
                            <a name="ch.2.8"></a>2.8
                        </dt>

                        <dd>
                            &nbsp;Tu apporteras l'offrande qui sera faite &agrave; l'&Eacute;ternel
                            avec ces choses-l&agrave;; elle sera remise au sacrificateur, qui la pr&eacute;sentera
                            sur l'autel.</dd>

                        <dt>
                            <a name="ch.2.9"></a>2.9
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur en pr&eacute;l&egrave;vera ce qui doit &ecirc;tre
                            offert comme souvenir, et le br&ucirc;lera sur l'autel. C'est une offrande
                            d'une agr&eacute;able odeur &agrave; l'&Eacute;ternel.</dd>

                        <dt>
                            <a name="ch.2.10"></a>2.10
                        </dt>

                        <dd>

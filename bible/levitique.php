                        </dt>

                        <dd>
                            &nbsp;Le huiti&egrave;me jour, elle prendra deux tourterelles ou deux jeunes
                            pigeons, et elle les apportera au sacrificateur, &agrave; l'entr&eacute;e
                            de la tente d'assignation.</dd>

                        <dt>
                            <a name="ch.15.30"></a>15.30
                        </dt>

                        <dd>
                            &nbsp;Le sacrificateur offrira l'un en sacrifice d'expiation, et l'autre
                            en holocauste; et le sacrificateur fera pour elle l'expiation devant l'&Eacute;ternel,
                            &agrave; cause du flux qui la rendait impure.</dd>

                        <dt>
                            <a name="ch.15.31"></a>15.31
                        </dt>

                        <dd>
                            &nbsp;Vous &eacute;loignerez les enfants d'Isra&euml;l de leurs impuret&eacute;s,
                            de peur qu'ils ne meurent &agrave; cause de leurs impuret&eacute;s, s'ils
                            souillent mon tabernacle qui est au milieu d'eux.</dd>

                        <dt>
                            <a name="ch.15.32"></a>15.32
                        </dt>

                        <dd>
                            &nbsp;Telle est la loi pour celui qui a une gonorrh&eacute;e ou qui est
                            souill&eacute; par une pollution,</dd>

                        <dt>
                            <a name="ch.15.33"></a>15.33
                        </dt>

                        <dd>
                            &nbsp;pour celle qui a son flux menstruel, pour l'homme ou la femme qui
                            a un flux, et pour l'homme qui couche avec une femme impure.</dd>
                    </dl>

